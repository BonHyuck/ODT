package android.study.ordertokmenu.InfoPackage;

public class SetInfo {
    private int categoryId;
    private int menuId;
    private int setNumber;
    private int setPrice;
    private String setKor;
    private String setEng;
    private String setChn;
    private String setJpn;

    public int getSetPrice() {
        return setPrice;
    }

    public void setSetPrice(int setPrice) {
        this.setPrice = setPrice;
    }

    public String getSetKor() {
        return setKor;
    }

    public void setSetKor(String setKor) {
        this.setKor = setKor;
    }

    public String getSetEng() {
        return setEng;
    }

    public void setSetEng(String setEng) {
        this.setEng = setEng;
    }

    public String getSetChn() {
        return setChn;
    }

    public void setSetChn(String setChn) {
        this.setChn = setChn;
    }

    public String getSetJpn() {
        return setJpn;
    }

    public void setSetJpn(String setJpn) {
        this.setJpn = setJpn;
    }

    public int getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(int categoryId) {
        this.categoryId = categoryId;
    }

    public int getMenuId() {
        return menuId;
    }

    public void setMenuId(int menuId) {
        this.menuId = menuId;
    }

    public int getSetNumber() {
        return setNumber;
    }

    public void setSetNumber(int setNumber) {
        this.setNumber = setNumber;
    }
}
