package android.study.ordertokpos.InfoPackage;

public class MenuInfo {
    private int menuId;
    private String menuKor;
    private String menuEng;
    private String menuChn;
    private String menuJpn;
    private int menuPrice;
    private int menuOption;
    private int categoryId;
    private String imgPath;

    public int getMenuId() {
        return menuId;
    }

    public void setMenuId(int menuId) {
        this.menuId = menuId;
    }

    public String getMenuKor() {
        return menuKor;
    }

    public void setMenuKor(String menuKor) {
        this.menuKor = menuKor;
    }

    public String getMenuEng() {
        return menuEng;
    }

    public void setMenuEng(String menuEng) {
        this.menuEng = menuEng;
    }

    public String getMenuChn() {
        return menuChn;
    }

    public void setMenuChn(String menuChn) {
        this.menuChn = menuChn;
    }

    public String getMenuJpn() {
        return menuJpn;
    }

    public void setMenuJpn(String menuJpn) {
        this.menuJpn = menuJpn;
    }

    public int getMenuPrice() {
        return menuPrice;
    }

    public void setMenuPrice(int menuPrice) {
        this.menuPrice = menuPrice;
    }

    public int getMenuOption() {
        return menuOption;
    }

    public void setMenuOption(int menuOption) {
        this.menuOption = menuOption;
    }

    public int getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(int categoryId) {
        this.categoryId = categoryId;
    }

    public String getImgPath() {
        return imgPath;
    }

    public void setImgPath(String imgPath) {
        this.imgPath = imgPath;
    }
}
